let numero = prompt('Digite um numero: ');

if (numero > 10) {
  console.log('o numero digitado é maior que 10');
} else if (numero == 10) {
  console.log('o numero digitado é 10');
} else if (numero < 10) {
  console.log('o numero digitado é menor que 10');
} else {
  console.log('Valor inválido');
}
